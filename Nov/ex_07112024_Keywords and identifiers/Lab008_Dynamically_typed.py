# Dynamically typed

# At the run time, data of the variable can be changed. and you don't need to
# mention the data type - Python  - Int is very smart

AGE = 34
age = 98
print(AGE)
print(age)
print(type(age))
print(type(AGE))

age = "Pramod" #variable type changed from int to string
print(type(age))
age = True  #variable type changed from string to boolean
print(type(age))

