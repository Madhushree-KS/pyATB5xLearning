# Operators
age = 65
# age is a variable name or identifier
# = is a assignment operator
# 65 is a variable value or literal

# Unary operator which is added to 1 operand
age = +65 # ignores + here
print(age)

age1 = -65 # prints -65 here
print(age1)

# - - unary operator -
how_many_cars_you_have = -1
how_many_cars_you_have = how_many_cars_you_have + 1
print(how_many_cars_you_have)

